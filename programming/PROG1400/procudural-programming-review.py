# Variables
# Assign the value 15 to the variable num
num = 15

# Check if num is even
# if/else
if num % 2 == 0:
    # If the condition is true, print that num is even
    print(f"{num} is even.")
else:
    # If the condition is false, print that num is odd
    print(f"{num} is odd.")

# Using a for loop to iterate over a range of numbers from 1 to 5
for i in range(1, 6):
    # Print the current value of i
    print(i)


# Initialize the variable num with the value 1
num = 1

# Start the while loop, which will run as long as num is less than or equal to 5
while num <= 5:
    # Print the current value of num
    print(num)
    # Increment num by 1
    num += 1

# Initialize the variable num with the value 1
num = 1

# Simulate a do-while loop using a while loop
while True:
    # Print the current value of num
    print(num)
    
    # Increment num by 1
    num += 1
    
    # Check the condition to exit the loop
    if num > 5:
        break

# Create a list of fruits

fruits = ["apple", "banana", "cherry", "date", "elderberry"]

# Loop through each element in the list
for fruit in fruits:
    # Print the current fruit
    print(fruit)




